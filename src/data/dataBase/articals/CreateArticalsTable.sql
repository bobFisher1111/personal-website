CREATE TABLE Articals (
	articalId int NOT NULL PRIMARY KEY,
    seriesId int FOREIGN KEY REFERENCES Series(seriesId),
    section varchar(255) NOT NULL,
    sectionLink varchar(255) NOT NULL,
    sectionType varchar(255) NOT NULL,
    authorId int FOREIGN KEY REFERENCES Authors(authorId),
	articalTitle varchar(255) NOT NULL,
    articalSubTitle varchar(255) NOT NULL,
    publishedDate date NOT NULL,
    numberOfFavorites varchar(255),
    series bit NOT NULL,
    seriesChapter INT NOT NULL,
    seriesType varchar(255) NOT NULL,
    useVideoInsteadOfImage bit NOT NULL,
    coverImageOrVideo varchar(255) NOT NULL,
    artical VARCHAR(MAX) NOT NULL,
);